package net.fexcraft.mod.lib.util.common;

import net.minecraft.item.EnumDyeColor;

public enum EnumColor{
	
	WHITE("white"), ORANGE("orange"), MAGENTA("magenta"), LIGHT_BLUE("light_blue"),
	YELLOW("yellow"), LIME("lime"), PINK("pink"), GRAY("gray"),
	LIGHT_GRAY("light_gray"), CYAN("cyan"), PURPLE("purple"), BLUE("blue"),
	BROWN("brown"), GREEN("green"), RED("red"), BLACK("black");
	
	private String name;
	
	EnumColor(String name){
		this.name = name;
	}
	
	public String getName(){
		return name;
	}
	
	public String toString(){
		return getName();
	}
	
	public static EnumColor fromString(String s){
		for(EnumColor color : EnumColor.values()){
			if(color.getName().equals(s)){
				return color;
			}
		}
		return EnumColor.WHITE;
	}
	
	public EnumDyeColor toDyeColor(){
		switch(this){
			case WHITE:      return EnumDyeColor.WHITE;
			case ORANGE:     return EnumDyeColor.ORANGE;
			case MAGENTA:    return EnumDyeColor.MAGENTA;
			case LIGHT_BLUE: return EnumDyeColor.LIGHT_BLUE;
			case YELLOW:     return EnumDyeColor.YELLOW;
			case LIME:       return EnumDyeColor.LIME;
			case PINK:       return EnumDyeColor.PINK;
			case GRAY:       return EnumDyeColor.GRAY;
			case LIGHT_GRAY: return EnumDyeColor.SILVER;
			case CYAN:       return EnumDyeColor.CYAN;
			case PURPLE:     return EnumDyeColor.PURPLE;
			case BLUE:       return EnumDyeColor.BLUE;
			case BROWN:      return EnumDyeColor.BROWN;
			case GREEN:      return EnumDyeColor.GREEN;
			case RED:        return EnumDyeColor.RED;
			case BLACK:      return EnumDyeColor.BLACK;
			default: return EnumDyeColor.WHITE;
		}
	}
	
	public static EnumColor fromDyeColor(EnumDyeColor color){
		switch(color){
			case BLACK:      return BLACK;
			case BLUE:       return BLUE;
			case BROWN:      return BROWN;
			case CYAN:       return CYAN;
			case GRAY:       return GRAY;
			case GREEN:      return GREEN;
			case LIGHT_BLUE: return LIGHT_BLUE;
			case LIME:       return LIME;
			case MAGENTA:    return MAGENTA;
			case ORANGE:     return ORANGE;
			case PINK:       return PINK;
			case PURPLE:     return PURPLE;
			case RED:        return RED;
			case SILVER:     return LIGHT_GRAY;
			case WHITE:      return WHITE;
			case YELLOW:     return YELLOW;
			default: return BLACK;
		}
	}

	public int toInt(){
		return toDyeColor().getMetadata();
	}
}